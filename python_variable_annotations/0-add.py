#!/usr/bin/env python3

"""
 a type-annotated function add that takes a float
 a and a float b as arguments and returns their sum as a float
"""


def add(a: float, b: float) -> float:
    """
    Calculate the sum of two float numbers.

    Args:
        a (float): The first input float.
        b (float): The second input float.

    Returns:
        float: The sum of a and b as a float.
    """
    return (a + b)
