#!/usr/bin/env python3

"""
type-annotated function concat that takes a string str1
and a string str2 as arguments and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    Calculate the sum of two strings.

    Args:
        str1 (str): The first input str.
        str2 (str): The second input str.

    Returns:
        str: The sum of str1 and str2 as a string.
    """
    return str1 + str2
